#include <unistd.h>

void	ft_putchar(char a[])
{
	a[2] = ' ';
	a[5] = ',';
	a[6] = ' ';
	if (a[3] == '0' && a[4] == '0')
	{
		return ;
	}
	if (a[0] == '0' && a[1] == '0' && a[3] == '0' && a[4] == '1')
	{
		write(1, a, 7);
	}
	if (a[3] == '0' && a[4] == '1')
	{
		return ;
	}
	if (a[0] == '9' && a[1] == '8' && a[3] == '9' && a[4] == '9')
	{
		write(1, a, 5);
	}
	else
	{
		write(1, a, 7);
	}
}

void	ft_print_comb2(void)
{
	char	a[7];

	a[0] = '0';
	while (a[0] <= '9')
	{
		a[1] = '0';
		while (a[1] <= '8')
		{
			a[3] = '0';
			while (a[3] <= '9')
			{
				a[4] = '0';
				while (a[4] <= '9')
				{
					ft_putchar(a);
					a[4]++;
				}
				a[3]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
